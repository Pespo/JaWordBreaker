package fr.imac.jwbreaker;

public class DrawableElement {
	private int m_x;
	private int m_y;
	private int m_width;
	private int m_height;
	
	public DrawableElement() {
		
	}

	public int x() {
		return m_x;
	}
	public int y() {
		return m_y;
	}
	public int width() {
		return m_width;
	}
	public int height() {
		return m_height;
	}
}
